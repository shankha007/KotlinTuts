fun main(args: Array<String>) {
    /*
     Conditional Operator -> return boolean i.e, true or false
     <
     >
     ==
     !=
     <=
     >=
    */
    var firstNum = 5
    var secondNum = 6
    println(firstNum > secondNum)
    println(firstNum < secondNum)
    println(firstNum == secondNum)
    println(firstNum != secondNum)
    println(firstNum >= secondNum)
    println(firstNum <= secondNum)
}