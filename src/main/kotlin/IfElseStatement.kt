fun main(args: Array<String>) {
    /*
     If Statements
    */
    var firstNum = 5
    var secondNum = 6

    if(firstNum > secondNum) {
        println("Yes, greater than")
    }

    if(firstNum < secondNum) {
        println("Yes, less than")
    }

    if(firstNum >= secondNum) {
        println("Yes")
    } else {
        println("No")
    }
}